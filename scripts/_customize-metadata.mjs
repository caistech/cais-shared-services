#!/usr/bin/env node
// Write a product's root metadata block from its portfolio-manifest.yaml
// product_registry entry. The worker behind scripts/customize-metadata.sh.
//
// Implements the SCAFFOLD-TIME METADATA CUSTOMIZATION rule (global CLAUDE.md /
// PRODUCT_STANDARDS §7): router-agnostic, reads the manifest as the source of
// truth, writes title/description/openGraph/twitter/icons, idempotent, and
// refuses to overwrite a hand-edited block (prints the intended output instead
// of silently corrupting work).
//
// Usage:
//   node _customize-metadata.mjs <manifest-path> <slug> <repo-dir> [--write]
//
// Without --write it is a dry run: it prints what it would do and the metadata
// block, and exits 0 (or 2 if a required field / safe edit is missing).

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { join } from "node:path";
import { parse } from "yaml";

const [, , manifestPath, slug, repoDir, ...rest] = process.argv;
const WRITE = rest.includes("--write");

if (!manifestPath || !slug || !repoDir) {
  console.error(
    "Usage: node _customize-metadata.mjs <manifest-path> <slug> <repo-dir> [--write]"
  );
  process.exit(1);
}

// ---- 1. Load the registry entry -------------------------------------------
if (!existsSync(manifestPath)) {
  console.error(`ERROR: manifest not found: ${manifestPath}`);
  process.exit(1);
}
const manifest = parse(readFileSync(manifestPath, "utf-8"));
const registry = manifest?.product_registry ?? {};
const entry = registry[slug];

if (!entry) {
  console.error(
    `ERROR: '${slug}' has no product_registry entry in ${manifestPath}.\n` +
      `       Add one (display_name + status fields) before running this.`
  );
  process.exit(2);
}

// Required to produce a complete metadata block. og_image is optional (we omit
// the image keys + warn if it's absent rather than blocking).
const missing = ["display_name", "tagline", "description", "canonical_url"].filter(
  (k) => !entry[k] || String(entry[k]).trim() === ""
);
if (missing.length) {
  console.error(
    `ERROR: product_registry.${slug} is missing required metadata field(s): ${missing.join(
      ", "
    )}.\n` +
      `       Fill them in ${manifestPath} (display_name is seeded; add tagline,\n` +
      `       description, canonical_url — and og_image for social cards).`
  );
  process.exit(2);
}

const {
  display_name: name,
  tagline,
  description,
  canonical_url: url,
  og_image: ogImage,
} = entry;
const icon = entry.favicon || "/favicon.ico";

if (!ogImage) {
  console.warn(
    `  ⚠ og_image not set for '${slug}' — Open Graph / Twitter image omitted (link previews will have no image).`
  );
}

// ---- 2. Build the metadata literal (App Router) ----------------------------
const js = (s) => JSON.stringify(String(s)); // safe-quote any string
const imagesBlock = ogImage
  ? `,
    images: [{ url: ${js(ogImage)}, width: 1200, height: 630, alt: ${js(name)} }]`
  : "";
const twImagesBlock = ogImage ? `,\n    images: [${js(ogImage)}]` : "";

const metadataBlock = `export const metadata: Metadata = {
  title: ${js(name)},
  description: ${js(description)},
  openGraph: {
    title: ${js(`${name} — ${tagline}`)},
    description: ${js(description)},
    url: ${js(url)},
    siteName: ${js(name)}${imagesBlock},
  },
  twitter: {
    card: "summary_large_image",
    title: ${js(`${name} — ${tagline}`)},
    description: ${js(description)}${twImagesBlock},
  },
  icons: { icon: ${js(icon)} },
};`;

// Scaffold-default sentinels that are always safe to overwrite.
const SCAFFOLD_TITLES = [
  "Create Next App",
  "Next.js",
  "create-next-app",
  "Generated by create next app",
];

// ---- 3. Locate the layout / document file ---------------------------------
const appLayout = [
  "src/app/layout.tsx",
  "app/layout.tsx",
  "src/app/layout.jsx",
  "app/layout.jsx",
]
  .map((p) => join(repoDir, p))
  .find(existsSync);

const pagesDoc = [
  "src/pages/_document.tsx",
  "pages/_document.tsx",
  "src/pages/_document.jsx",
  "pages/_document.jsx",
  "src/pages/_document.js",
  "pages/_document.js",
]
  .map((p) => join(repoDir, p))
  .find(existsSync);

if (!appLayout && !pagesDoc) {
  console.error(
    `ERROR: no App Router layout (src/app/layout.tsx) or Pages Router\n` +
      `       _document found under ${repoDir}. Pass the correct repo dir as\n` +
      `       the 3rd argument.`
  );
  process.exit(2);
}

// ---- 4a. App Router path ---------------------------------------------------
if (appLayout) {
  const src = readFileSync(appLayout, "utf-8");

  // Find an existing `export const metadata ... = { ... }` and brace-match it.
  const startMatch = src.match(/export\s+const\s+metadata\b[^=]*=\s*{/);
  let action, next;

  if (startMatch) {
    const objStart = startMatch.index + startMatch[0].length - 1; // at the `{`
    const end = matchBrace(src, objStart);
    if (end === -1) {
      fail(`Could not brace-match the existing 'export const metadata' in ${appLayout}.`);
    }
    // Consume a trailing `as Metadata` / `satisfies Metadata` / `;`.
    let after = end + 1;
    const tail = src.slice(after).match(/^\s*(as\s+\w+|satisfies\s+\w+)?\s*;?/);
    if (tail) after += tail[0].length;

    const existing = src.slice(startMatch.index, after);
    const titleMatch = existing.match(/title\s*:\s*(["'`])([^"'`]*)\1/);
    const existingTitle = titleMatch ? titleMatch[2] : null;

    if (existingTitle === name) {
      console.log(`  · ${slug}: metadata already customized (title="${name}") — no-op.`);
      process.exit(0);
    }
    if (existingTitle !== null && !SCAFFOLD_TITLES.includes(existingTitle)) {
      console.error(
        `  ⚠ ${slug}: existing metadata title is "${existingTitle}" — not the\n` +
          `    manifest display_name and not a scaffold default. Refusing to\n` +
          `    overwrite a hand-edited block. Intended block:\n`
      );
      console.error(indent(metadataBlock));
      process.exit(2);
    }
    action = `replace metadata block (was title="${existingTitle ?? "<none>"}")`;
    next = src.slice(0, startMatch.index) + metadataBlock + src.slice(after);
  } else {
    // Inject: ensure the Metadata type import, then place the block after the
    // last top-level import.
    action = "inject new metadata block";
    let body = src;
    if (!/import\s+type\s+\{[^}]*\bMetadata\b[^}]*\}\s+from\s+["']next["']/.test(body)) {
      body = `import type { Metadata } from "next";\n` + body;
    }
    const lastImport = [...body.matchAll(/^\s*import\b.*$/gm)].pop();
    const at = lastImport ? lastImport.index + lastImport[0].length : 0;
    next = body.slice(0, at) + `\n\n${metadataBlock}\n` + body.slice(at);
  }

  emit(appLayout, src, next, action, "App Router");
  process.exit(0);
}

// ---- 4b. Pages Router path -------------------------------------------------
{
  const src = readFileSync(pagesDoc, "utf-8");
  const titleMatch = src.match(/<title>([^<]*)<\/title>/);
  const existingTitle = titleMatch ? titleMatch[1].trim() : null;

  if (existingTitle === name) {
    console.log(`  · ${slug}: <title> already "${name}" — no-op.`);
    process.exit(0);
  }
  if (existingTitle !== null && existingTitle !== "" && !SCAFFOLD_TITLES.includes(existingTitle)) {
    console.error(
      `  ⚠ ${slug}: existing <title> is "${existingTitle}" — not the display_name\n` +
        `    and not a scaffold default. Refusing to overwrite a hand-edited\n` +
        `    title. Set it manually to: ${name}`
    );
    process.exit(2);
  }

  const headInject =
    `<title>${name}</title>\n` +
    `        <meta name="description" content=${js(description)} />\n` +
    `        <meta property="og:title" content=${js(`${name} — ${tagline}`)} />\n` +
    `        <meta property="og:description" content=${js(description)} />\n` +
    `        <meta property="og:url" content=${js(url)} />\n` +
    (ogImage ? `        <meta property="og:image" content=${js(ogImage)} />\n` : "") +
    `        <meta name="twitter:card" content="summary_large_image" />`;

  let next, action;
  if (titleMatch) {
    action = `replace <title> (was "${existingTitle}")`;
    next = src.replace(/<title>[^<]*<\/title>/, `<title>${name}</title>`);
  } else if (/<Head>/.test(src)) {
    action = "inject title + meta into <Head>";
    next = src.replace(/<Head>/, `<Head>\n        ${headInject}`);
  } else {
    console.error(
      `ERROR: ${pagesDoc} has neither <title> nor <Head> — add a next/document\n` +
        `       <Head> and re-run.`
    );
    process.exit(2);
  }

  emit(pagesDoc, src, next, action, "Pages Router");
  process.exit(0);
}

// ---- helpers ---------------------------------------------------------------
function matchBrace(text, openIdx) {
  let depth = 0;
  for (let i = openIdx; i < text.length; i++) {
    if (text[i] === "{") depth++;
    else if (text[i] === "}") {
      depth--;
      if (depth === 0) return i;
    }
  }
  return -1;
}
function indent(s) {
  return s.replace(/^/gm, "    ");
}
function fail(msg) {
  console.error(`ERROR: ${msg}`);
  process.exit(2);
}
function emit(file, before, after, action, router) {
  if (before === after) {
    console.log(`  · ${slug}: ${file} already current — no-op.`);
    return;
  }
  console.log(`  ${router}: ${file}`);
  console.log(`  Action: ${action}`);
  if (WRITE) {
    writeFileSync(file, after, "utf-8");
    console.log(`  ✓ written.`);
  } else {
    console.log(`  (dry run — re-run with --write to apply) Intended metadata:`);
    console.log(indent(metadataBlock));
  }
}
